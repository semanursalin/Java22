
public class Loops {

	public static void main(String[] args) {
		//For
				for (int i = 0; i < 10; i++) {
					
				System.out.println("Bıhtıımm");
				}
				System.out.println("Annee For bitti");
				//While
				int i=1;
				while (i<10) { //sonsuz döngü
					System.out.println(i);
					i+=2;
				}
				System.out.println("Annee While bitti");
				//Do-While : şart sağlamasa bile komut 1 kere çalışır
				int j=2;
				do {
					System.out.println(j);
					j+=2;
				}
						
					while (j<10);
				System.out.println("Annee Do-While bitti");

	}

}

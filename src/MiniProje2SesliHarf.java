
public class MiniProje2SesliHarf {

	public static void main(String[] args) {
		char harf='a';
		
		switch (harf) {
		case'A','a':
		case'I','ı':
		case'O','o':
		case'U','u':
		System.out.println("Kalın sesli harf");
		break;
		default:
		System.out.println("İnce sesli harf");


	}

}
}
package Datatypes;

public class Datatypes {

	public static void main(String[] args) {
		int tamSayi = 20 ;
		tamSayi = 21;
		System.out.println(tamSayi);
		byte sayi = 127;
		sayi = 125;
		System.out.println(sayi);
		long sayiLo = 15645658;
		sayiLo = 15151515;
		System.out.println(sayiLo);
		float sayi2 = 52;
		sayi2 = 51;
		System.out.println(sayi2);
		// char single character
		char karak = 'E';
		System.out.println(karak);
		// boolean 0-1 
		boolean evet = true;


	}

}
